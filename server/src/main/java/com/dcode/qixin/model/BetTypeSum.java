package com.dcode.qixin.model;

import java.util.Date;

public class BetTypeSum {
    private Integer sysId;

    private Integer periodId;

    private String playType;

    private Integer playSum;

    private Date createdTime;

    private Date modifyTime;

    public Integer getSysId() {
        return sysId;
    }

    public void setSysId(Integer sysId) {
        this.sysId = sysId;
    }

    public Integer getPeriodId() {
        return periodId;
    }

    public void setPeriodId(Integer periodId) {
        this.periodId = periodId;
    }

    public String getPlayType() {
        return playType;
    }

    public void setPlayType(String playType) {
        this.playType = playType == null ? null : playType.trim();
    }

    public Integer getPlaySum() {
        return playSum;
    }

    public void setPlaySum(Integer playSum) {
        this.playSum = playSum;
    }

    public Date getCreatedTime() {
        return createdTime;
    }

    public void setCreatedTime(Date createdTime) {
        this.createdTime = createdTime;
    }

    public Date getModifyTime() {
        return modifyTime;
    }

    public void setModifyTime(Date modifyTime) {
        this.modifyTime = modifyTime;
    }
}